#define DIV_ROUND_UP(n,d) (((n) + (d) - 1) / (d))
#define ROUND(a,b)		(((a) + (b) - 1) & ~((b) - 1))

/* The `const' in roundup() prevents gcc-3.3 from calling __divdi3 */
#define roundup(x, y) (					\
{							\
	const typeof(y) __y = y;			\
	(((x) + (__y - 1)) / __y) * __y;		\
}							\
)
#define rounddown(x, y) (				\
{							\
	typeof(x) __x = (x);				\
	__x - (__x % (y));				\
}							\
)

